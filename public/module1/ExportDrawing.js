async function exportEngineeringDrawings(paperSize = 'a4') {
    if (!window.jspdf) {
        alert("jsPDF library not found.");
        return;
    }

    const rawData = localStorage.getItem('solarBOMData');
    if (!rawData) {
        alert("No Project Data found. Please Calculate BOM first.");
        return;
    }
    const data = JSON.parse(rawData);

    // 1. Define Modules with IDs that match the checkbox values
    const drawings = [
        { id: "top", title: "Solar Panels Top Layout", module: (typeof DrawSolarLayout !== 'undefined') ? DrawSolarLayout : null },
        { id: "structure", title: "Structural Top View", module: (typeof DrawStructuralTop !== 'undefined') ? DrawStructuralTop : null },
        { id: "dims", title: "Rafter and Purlin Dimensions", module: (typeof DrawRafterPurlin !== 'undefined') ? DrawRafterPurlin : null },
        { id: "gaps", title: "Vertical Column/Pole Centre Spacing", module: (typeof DrawColumnGap !== 'undefined') ? DrawColumnGap : null },
        { id: "side", title: "Structure Side (Slope N-S) View", module: (typeof SideProfile !== 'undefined') ? SideProfile : null }
    ];

    // 2. Filter list based on both "is it active" AND "is it checked"
    const checkedIds = Array.from(document.querySelectorAll('.export-check:checked')).map(cb => cb.value);
    const activeList = drawings.filter(d => d.module !== null && checkedIds.includes(d.id));

    if (activeList.length === 0) {
        alert("Please select at least one drawing to export.");
        return;
    }

    const { jsPDF } = window.jspdf;
    const doc = new jsPDF('l', 'mm', paperSize);
    const pageW = doc.internal.pageSize.getWidth();
    const pageH = doc.internal.pageSize.getHeight();
    const margin = 10;

    const btn = document.getElementById('btnExportDrawings');
    const originalText = btn ? btn.innerText : "Export PDF";
    if(btn) btn.innerText = "Processing...";

    try {
        const logoImg = await loadLogo();

        for (let i = 0; i < activeList.length; i++) {
            const d = activeList[i];
            if (i > 0) doc.addPage();

            // 1. TITLE BAR
            doc.setFillColor(0, 0, 0); 
            doc.rect(margin, margin, pageW - (margin*2) - 50, 10, 'F');
            doc.setTextColor(255, 255, 255);
            doc.setFont("helvetica", "bold");
            doc.setFontSize(12);
            doc.text(d.title.toUpperCase(), margin + 5, margin + 7);

            // 2. DRAWING IMAGE
            const svgStr = d.module.draw();
            const imgObj = await svgToImage(svgStr);

            const topSpace = margin + 15; 
            const bottomSpace = 35; 
            const availW = pageW - (margin * 2);
            const availH = pageH - topSpace - bottomSpace;

            const ratioW = availW / imgObj.width;
            const ratioH = availH / imgObj.height;
            const scale = Math.min(ratioW, ratioH);
            const finalW = imgObj.width * scale;
            const finalH = imgObj.height * scale;
            const finalX = margin + (availW - finalW) / 2;
            const finalY = topSpace + (availH - finalH) / 2;

            doc.addImage(imgObj.data, 'JPEG', finalX, finalY, finalW, finalH, undefined, 'Medium');
            
            // 3. LOGO (Top Right in the whitespace)
            if (logoImg) {
                // Placed at pageW - 45 (Right side)
                doc.addImage(logoImg, 'PNG', pageW - 45, 4, 35, 12);
            }// 4. FOOTER (Moved here so it ALWAYS shows)
            doc.setFontSize(9);
            doc.setTextColor(100, 100, 100);
            doc.setFont("helvetica", "italic");
            doc.text("Generated by www.solarstruktura.com", margin, pageH - margin - 5);
            doc.text("FOR CONCEPTUAL USE ONLY. No structural reinforcement or wind load analysis provided.", margin, pageH - margin + 8);

            // 4. HORIZONTAL TABLE
            drawHorizontalTable(doc, pageW - margin, pageH - margin, data);
        }

       // --- NEW: Calculate Actual Capacity for Filename (Accounting for Air Pockets) ---
        const baseTotalPanels = (parseFloat(data.PVQtyNS) || 0) * (parseFloat(data.PVQtyEW) || 0);
        const airPocketsCount = (window.solarAirPockets && window.solarAirPockets.size) ? window.solarAirPockets.size : 0;
        const actualPanels = Math.max(0, baseTotalPanels - airPocketsCount);

        let actualCapKW = 0;
        if (baseTotalPanels > 0 && data.SystemCapacityKW) {
            actualCapKW = (data.SystemCapacityKW / baseTotalPanels) * actualPanels;
        }
        
        const capKW = actualCapKW > 0 ? actualCapKW.toFixed(2) : "0.00";
        const safeName = (data.CustName || "Customer").replace(/[^a-z0-9]/gi, '');
        const safeLoc = (data.CustLoc || "Location").replace(/[^a-z0-9]/gi, '');
        
        doc.save(`SolarStruktura_Drawing_${capKW}KW_${safeName}_${safeLoc}.pdf`);

    } catch (e) {
        console.error(e);
        alert("Error: " + e.message);
    } finally {
        if(btn) btn.innerText = originalText;
    }
}

function svgToImage(svgStr) {
    return new Promise((resolve) => {
        const parser = new DOMParser();
        const doc = parser.parseFromString(svgStr, "image/svg+xml");
        const svg = doc.documentElement;
        const vb = svg.getAttribute('viewBox').split(' ');
        const vbW = parseFloat(vb[2]);
        const vbH = parseFloat(vb[3]);
        
        // --- QUALITY IMPROVEMENT ---
        // Increasing scale to 4.0 makes lines much sharper in PDF
        const scale = 4.0; 
        const cvsW = vbW * scale;
        const cvsH = vbH * scale;

        svg.setAttribute('width', cvsW);
        svg.setAttribute('height', cvsH);

        const xml = new XMLSerializer().serializeToString(svg);
        const img = new Image();
        img.onload = () => {
            const canvas = document.createElement('canvas');
            canvas.width = cvsW;
            canvas.height = cvsH;
            const ctx = canvas.getContext('2d');
            
            // White background
            ctx.fillStyle = "#FFFFFF";
            ctx.fillRect(0, 0, canvas.width, canvas.height);
            
            // Draw at full size (No padding/No Zoom-out)
            ctx.drawImage(img, 0, 0, cvsW, cvsH);
            
            // Export as high-quality JPEG (0.95 quality)
            resolve({ 
                data: canvas.toDataURL("image/jpeg", 0.95), 
                width: vbW, 
                height: vbH 
            });
        };
        img.src = "data:image/svg+xml;base64," + btoa(unescape(encodeURIComponent(xml)));
    });
}
function loadLogo() {
    return new Promise((resolve) => {
        // 1. Check if global variable exists
        if (typeof COMPANY_LOGO_SVG === 'undefined' || !COMPANY_LOGO_SVG) {
            resolve(null);
            return;
        }
        // 2. Convert SVG string to PNG Data URL
        const img = new Image();
        const svg64 = btoa(unescape(encodeURIComponent(COMPANY_LOGO_SVG)));
        img.src = "data:image/svg+xml;base64," + svg64;
        
        img.onload = () => {
            const canvas = document.createElement('canvas');
            // Set canvas to match your logo's viewBox (1000x353)
            canvas.width = 1000;
            canvas.height = 353;
            const ctx = canvas.getContext('2d');
            ctx.drawImage(img, 0, 0, 1000, 353);
            resolve(canvas.toDataURL("image/png"));
        };
        img.onerror = () => resolve(null);
    });
}
function drawHorizontalTable(doc, x, y, data) {
    // Increased table width from 180 to 210 to comfortably fit the new column
    const tableW = 210; 
    const cellH = 7; 
    const startX = x - tableW;
    const startY = y - (cellH * 2);

    // --- NEW: Calculate Actual Panels & Capacity (Accounting for Air Pockets) ---
    const baseTotalPanels = (parseFloat(data.PVQtyNS) || 0) * (parseFloat(data.PVQtyEW) || 0);
    
    // Count how many panels the user clicked/hid
    const airPocketsCount = (window.solarAirPockets && window.solarAirPockets.size) ? window.solarAirPockets.size : 0;
    const actualPanels = Math.max(0, baseTotalPanels - airPocketsCount);

    // Recalculate KW Capacity based on the new panel count
    let actualCapKW = 0;
    if (baseTotalPanels > 0 && data.SystemCapacityKW) {
        // Find the KW per panel, then multiply by remaining panels
        actualCapKW = (data.SystemCapacityKW / baseTotalPanels) * actualPanels;
    }

    const cap = actualCapKW.toFixed(2) + " kW";
    const panelModel = data.PMod || "-";
    const panelQtyString = `${panelModel} x ${actualPanels}`;

    const cols = [
        { header: "CUSTOMER NAME",   val: (data.CustName || "-").substring(0,25), w: 40 },
        { header: "LOCATION",        val: (data.CustLoc || "-").substring(0,25),  w: 35 },
        { header: "CAPACITY",        val: cap,                                    w: 25 },
        { header: "SOLAR PANEL QTY", val: panelQtyString,                         w: 40 }, // <-- UPDATED COLUMN
        { header: "DESIGN TYPE",     val: (data.DrawingType || "2D Solar Structure"), w: 40 },
        { header: "COMPANY",         val: (data.CompName || "-").substring(0,25), w: 30 } 
    ];

    let currentX = startX;
    doc.setFontSize(8);
    doc.setLineWidth(0.2);

    for(let i=0; i<cols.length; i++) {
        const col = cols[i];
        
        // Draw Header
        doc.setFillColor(0, 0, 0);
        doc.rect(currentX, startY, col.w, cellH, 'F');
        doc.setTextColor(255, 255, 255);
        doc.setFont("helvetica", "bold");
        doc.text(col.header, currentX + (col.w/2), startY + 4.5, { align: "center" });

        // Draw Value Cell
        doc.setFillColor(255, 255, 255);
        doc.setDrawColor(0, 0, 0);
        doc.rect(currentX, startY + cellH, col.w, cellH, 'FD');
        doc.setTextColor(0, 0, 0);
        doc.setFont("helvetica", "normal");
        doc.text(String(col.val), currentX + (col.w/2), startY + cellH + 4.5, { align: "center" });
        
        currentX += col.w;
    }
}